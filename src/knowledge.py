# ==================== 检索引擎 ====================


class TravelKnowledgeRetriever:
    """
    基于 BM25 变体的旅行知识检索器。

    核心特性：
      - TF-IDF 风格的关键词匹配
      - 城市名称精确匹配加权
      - 类别过滤支持
      - 结果排序 + Top-K 截断

    用法:
        retriever = TravelKnowledgeRetriever()
        results = retriever.search("长沙有什么好吃的", city="长沙", top_k=5)
    """

    def __init__(self, knowledge_base: list[TravelTip] | None = None):
        self.kb = knowledge_base or KNOWLEDGE_BASE

    def _tokenize(self, text: str) -> set[str]:
        """中文分词简化版：按 2-gram + 关键词切分"""
        # 提取中文字符作为 token
        chinese = re.findall(r'[一-鿿]+', text.lower())
        tokens = set()
        for word in chinese:
            # 2-gram
            for i in range(len(word) - 1):
                tokens.add(word[i:i + 2])
            # 完整词
            if len(word) <= 4:
                tokens.add(word)
        # 英文关键词
        english = re.findall(r'[a-z]+', text.lower())
        tokens.update(english)
        return tokens

    def search(
        self,
        query: str,
        city: str = "",
        category: str = "",
        top_k: int = 5,
        min_score: float = 0.1,
    ) -> list[TravelTip]:
        """
        检索旅行知识。

        Args:
            query: 用户查询文本
            city: 限定城市（空字符串 = 不限）
            category: 限定类别
            top_k: 返回结果数
            min_score: 最低分数阈值
        """
        query_tokens = self._tokenize(query)
        city_tokens = self._tokenize(city) if city else set()

        scored = []
        for tip in self.kb:
            score = 0.0

            # 关键词匹配（TF component）
            tip_tokens = set(tip.keywords)
            matched = query_tokens & tip_tokens
            if matched:
                score += len(matched) / len(tip_tokens) * 3.0

            # 城市精确匹配加权
            if city and tip.city != "通用":
                if city in tip.city or tip.city in city:
                    score += 2.0
                elif city_tokens & {tip.city}:
                    score += 1.5

            # 通用知识对所有查询都有基础相关性
            if tip.city == "通用":
                score += 0.5

            # 内容文本匹配（IDF-like）
            content_tokens = self._tokenize(tip.content)
            content_match = query_tokens & content_tokens
            if content_match:
                score += len(content_match) * 0.5

            # 应用权重
            score *= tip.weight

            if score >= min_score:
                scored.append((score, tip))

        # 按分数降序 + Top-K
        scored.sort(key=lambda x: x[0], reverse=True)
        return [tip for _, tip in scored[:top_k]]

    def format_context(self, tips: list[TravelTip]) -> str:
        """将检索结果格式化为 Agent 可读的上下文。"""
        if not tips:
            return "未找到相关旅行知识。"
        lines = []
        for i, tip in enumerate(tips, 1):
            tag = f"[{tip.city}·{tip.category}]" if tip.city != "通用" else f"[通用·{tip.category}]"
            lines.append(f"{i}. **{tag}** {tip.content}")
        return "\n\n".join(lines)


# 全局检索器单例
_retriever: TravelKnowledgeRetriever | None = None


def get_retriever() -> TravelKnowledgeRetriever:
    global _retriever
    if _retriever is None:
        _retriever = TravelKnowledgeRetriever()
    return _retriever


def search_travel_knowledge(query: str, city: str = "", top_k: int = 5) -> str:
    """便捷函数 —— 供 Agent Tool 调用。"""
    retriever = get_retriever()
    tips = retriever.search(query, city=city, top_k=top_k)
    return retriever.format_context(tips)
